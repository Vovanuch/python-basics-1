'''

12 месяцев

Решите уравнение в натуральных числах 28n+30k+31m=36528n + 30 k + 31 m = 36528n+30k+31m=365.

Примечание. Используйте вложенный цикл for. В первую очередь запишите решение с наименьшим значением nnn.
'''

# 28n + 30k + 31m = 365
# n max = 365/28 = 13
# k max = 365 / 30 = 12
# m max = 365 / 31 = 11

for n in range (14):
    for k in range (13):
        for m in range (12):
            if (28*n + 30*k + 31*m == 365):
                print (f'Solution: n = {n}, k = {k}, m = {m}')