def print_star_row(n):
    print('*' * n)

def print_star_row_up(m, n):
    for i in range(m, n+1):
        print_star_row(i)
        
def print_star_row_down(m, n):
    for i in range(m, n-1, -1):
        print_star_row(i)


n = int(input().strip())
print_star_row_up(1, n // 2)
print_star_row_down((n // 2) + 1, 1)

