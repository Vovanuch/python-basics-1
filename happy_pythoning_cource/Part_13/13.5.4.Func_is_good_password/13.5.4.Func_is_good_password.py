#–µ–≥–æ –¥–ª–∏–Ω–∞ –Ω–µ –º–µ–Ω–µ–µ 8 —Å–∏–º–≤–æ–ª–æ–≤; 
def is_good_len(password):
    return (len(password) >= 8)

#–æ–Ω —Å–æ–¥–µ—Ä–∂–∏—Ç –∫–∞–∫ –º–∏–Ω–∏–º—É–º –æ–¥–Ω—É –∑–∞–≥–ª–∞–≤–Ω—É—é –±—É–∫–≤—É (–≤–µ—Ä—Ö–Ω–∏–π —Ä–µ–≥–∏—Å—Ç—Ä); 
def is_capital_symbol(password):
    res = False
    for c in password:
        if 'Z' >= c >= 'A':
            res = True
            break
    return res


#–æ–Ω —Å–æ–¥–µ—Ä–∂–∏—Ç –∫–∞–∫ –º–∏–Ω–∏–º—É–º –æ–¥–Ω—É —Å—Ç—Ä–æ—á–Ω—É—é –±—É–∫–≤—É (–Ω–∏–∂–Ω–∏–π —Ä–µ–≥–∏—Å—Ç—Ä);
def is_little_symbol(password):
    res = False
    for c in password:
        if 'z' >= c >= 'a':
            res = True
            break
    return res


#–æ–Ω —Å–æ–¥–µ—Ä–∂–∏—Ç —Ö–æ—Ç—è –±—ã –æ–¥–Ω—É —Ü–∏—Ñ—Ä—É.
def is_digit_symbol(password):
    res = False
    for c in password:
        if '9' >= c >= '0':
            res = True
            break
    return res


# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def is_password_good(password):
    if is_good_len(password) and is_capital_symbol(password) and is_little_symbol(password) and is_digit_symbol(password):
        return True
    return False

