# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def number_to_words(num):
    dict_edin = {1:'–æ–¥–∏–Ω', 2:'–¥–≤–∞', 3:'—Ç—Ä–∏', 4:'—á–µ—Ç—ã—Ä–µ', 5:'–ø—è—Ç—å', 6:'—à–µ—Å—Ç—å', 7:'—Å–µ–º—å', 8:'–≤–æ—Å–µ–º—å', 9:'–¥–µ–≤—è—Ç—å', 
                 10:'–¥–µ—Å—è—Ç—å', 11:'–æ–¥–∏–Ω–Ω–∞–¥—Ü–∞—Ç—å', 12:'–¥–≤–µ–Ω–∞–¥—Ü–∞—Ç—å', 13:'—Ç—Ä–∏–Ω–∞–¥—Ü–∞—Ç—å', 14:'—á–µ—Ç—ã—Ä–Ω–∞–¥—Ü–∞—Ç—å', 15:'–ø—è—Ç–Ω–∞–¥—Ü–∞—Ç—å', 
                 16:'—à–µ—Å—Ç–Ω–∞–¥—Ü–∞—Ç—å', 17:'—Å–µ–º–Ω–∞–¥—Ü–∞—Ç—å', 18:'–≤–æ—Å–µ–º–Ω–∞–¥—Ü–∞—Ç—å', 19:'–¥–µ–≤—è—Ç–Ω–∞–¥—Ü–∞—Ç—å'}
    dict_des = {2:'–¥–≤–∞–¥—Ü–∞—Ç—å', 3:'—Ç—Ä–∏–¥—Ü–∞—Ç—å', 4:'—Å–æ—Ä–æ–∫', 5:'–ø—è—Ç—å–¥–µ—Å—è—Ç', 6:'—à–µ—Å—Ç—å–¥–µ—Å—è—Ç', 7:'—Å–µ–º—å–¥–µ—Å—è—Ç', 
                7:'—Å–µ–º—å–¥–µ—Å—è—Ç', 8:'–≤–æ—Å–µ–º—å–¥–µ—Å—è—Ç', 9:'–¥–µ–≤—è–Ω–æ—Å—Ç–æ'}
    
    if num in dict_edin.keys():
        return dict_edin[num]
    elif num % 10 == 0:
        return dict_des[num // 10]
    else:
        res = dict_des[num // 10] + ' ' + dict_edin[num % 10]
        return res
    

